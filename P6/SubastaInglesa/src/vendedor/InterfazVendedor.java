    public void actualizarSubasta(Subasta subasta) {

        switch (subasta.getEstado()) {
            // Se a subasta esta en curso, actualizamos os valores da taboa
            case Subasta.ESTADO_1:

                for (int i = 0; i < taboaSubastas.getRowCount(); i++) {
                    if (taboaSubastas.getValueAt(i, 0).equals(subasta.getIdSubasta())) {
                        taboaSubastas.setValueAt(subasta.getRonda(), i, 2);
                        taboaSubastas.setValueAt(subasta.getPrezoActual(), i, 3);
                        taboaSubastas.setValueAt(subasta.getGanhador(), i, 4);
                        taboaSubastas.setValueAt(subasta.getEstado(), i, 5);
                        break;
                    }
                }
                break;
            // Se a subasta finaliza, actualizamos os valores da taboa e 
            // engadimos a lista de libros vendidos o libro
            case Subasta.ESTADO_2:

                for (int i = 0; i < taboaSubastas.getRowCount(); i++) {
                    if (taboaSubastas.getValueAt(i, 0).equals(subasta.getIdSubasta())) {
                        taboaSubastas.setValueAt(subasta.getRonda(), i, 2);
                        taboaSubastas.setValueAt(subasta.getPrezoActual(), i, 3);
                        taboaSubastas.setValueAt(subasta.getGanhador(), i, 4);
                        taboaSubastas.setValueAt(subasta.getEstado(), i, 5);
                        break;
                    }
                }
                // Engadimos a lista de libros vendidos o libro
                ((DefaultListModel) listaSubastas.getModel()).add(0, subasta.getTitulo() + " | " + subasta.getPrezoActual() + " â‚¬ | " + subasta.getGanhador());
                break;
            // En caso de que a subasta ainda non estea en curso, engadimos a 
            // subasta a taboa se non estaba
            case Subasta.ESTADO_0:

                int index = -1;
                for (int i = 0; i < taboaSubastas.getRowCount(); i++) {
                    if (taboaSubastas.getValueAt(i, 0).equals(subasta.getIdSubasta())) {
                        index = i;
                        break;
                    }
                }
                // Se a subasta non esta na taboa engadimola
                if (index == -1) {
                    Object[] fila = new Object[]{subasta.getIdSubasta(), subasta.getTitulo(), subasta.getRonda(), subasta.getPrezoActual(), subasta.getGanhador(), subasta.getEstado()};
                    ((DefaultTableModel) taboaSubastas.getModel()).addRow(fila);
                }
                break;
        }
    }

