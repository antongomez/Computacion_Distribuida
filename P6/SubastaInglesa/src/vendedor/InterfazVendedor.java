            vendedor.actualizarCatalogo(txtTitulo.getText(), Float.parseFloat(txtPrezo.getText()), Integer.parseInt(txtIncremento.getText()));
            txtTitulo.setText("");
            txtPrezo.setText("");
            txtIncremento.setText("");
